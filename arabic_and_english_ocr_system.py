class CRNNTrOCR(nn.Module):
    def __init__(self, vocab_size, pad_id, d_model=256):
        super().__init__()

        # CNN feature extractor
        self.cnn = nn.Sequential(
            nn.Conv2d(1, 64, kernel_size=3, padding=1),
            nn.ReLU(),
            nn.MaxPool2d(2, 2),

            nn.Conv2d(64, 128, kernel_size=3, padding=1),
            nn.ReLU(),
            nn.MaxPool2d(2, 2),

            nn.Conv2d(128, 256, kernel_size=3, padding=1),
            nn.ReLU(),
            nn.MaxPool2d((2, 1))
        )

        # BiLSTM sequence encoder
        self.rnn = nn.LSTM(
            input_size=256 * 8,
            hidden_size=256,
            num_layers=2,
            bidirectional=True,
            batch_first=True,
            dropout=0.2
        )

        self.dropout = nn.Dropout(0.3)

        self.feature_projection = nn.Linear(512, d_model)

        self.embedding = nn.Embedding(vocab_size, d_model)
        self.pos_embedding = nn.Embedding(200, d_model)

        decoder_layer = nn.TransformerDecoderLayer(
            d_model=d_model,
            nhead=4,
            batch_first=True,
            dropout=0.3
        )

        self.decoder = nn.TransformerDecoder(
            decoder_layer,
            num_layers=3
        )

        self.fc = nn.Linear(d_model, vocab_size)

        self.pad_id = pad_id

    def forward(self, images, target_seq):
        x = self.cnn(images)

        b, c, h, w = x.shape

        x = x.permute(0, 3, 1, 2)
        x = x.reshape(b, w, c * h)

        x, _ = self.rnn(x)

        x = self.dropout(x)

        memory = self.feature_projection(x)

        seq_len = target_seq.size(1)

        positions = torch.arange(seq_len).unsqueeze(0).to(target_seq.device)

        tgt = self.embedding(target_seq) + self.pos_embedding(positions)

        tgt_mask = nn.Transformer.generate_square_subsequent_mask(
            seq_len
        ).to(target_seq.device)

        tgt_key_padding_mask = (target_seq == self.pad_id)

        out = self.decoder(
            tgt=tgt,
            memory=memory,
            tgt_mask=tgt_mask,
            tgt_key_padding_mask=tgt_key_padding_mask
        )

        logits = self.fc(out)

        return logits

