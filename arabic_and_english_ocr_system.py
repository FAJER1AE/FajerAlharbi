# Main OCR Function
def run_complete_ocr(image):

    if image is None:
        return (
            "Please upload image",
            "",
            None,
            None
        )

    # Auto Language Detection
    detected_lang = detect_language_from_image(image)

    # Arabic Model
    if detected_lang == "arabic":

        predicted_text, steps = predict_arabic_topk(
            arabic_model,
            image,
            top_k=3
        )

        language_name = "Arabic"

    # English Model
    else:

        predicted_text, steps = predict_english_topk(
            image,
            top_k=3,
            show=False
        )

        language_name = "English"

    # Format Top-K Results
    topk_output = ""

    for i, step in enumerate(steps[:len(predicted_text)]):

        alternatives = list(
            zip(
                step["top_chars"],
                step["top_probs"]
            )
        )

        topk_output += (
            f"Position {i+1:02d}\n"
            f"{alternatives}\n\n"
        )

    # Save Results
    timestamp = datetime.now().strftime("%Y-%m-%d_%H-%M-%S")

    result_data = {
        "timestamp": timestamp,
        "system_name": "DocOCR Vision",
        "language": language_name,
        "predicted_text": predicted_text,
        "topk_predictions": steps
    }

    # Save JSON
    json_path = (
        f"saved_results/json/"
        f"ocr_result_{timestamp}.json"
    )

    with open(json_path, "w", encoding="utf-8") as f:
        json.dump(
            result_data,
            f,
            ensure_ascii=False,
            indent=4
        )

    # Save CSV
    csv_data = []

    for i, step in enumerate(steps):

        csv_data.append({
            "position": i + 1,
            "top_chars": str(step["top_chars"]),
            "top_probs": str(step["top_probs"])
        })

    csv_df = pd.DataFrame(csv_data)

    csv_path = (
        f"saved_results/csv/"
        f"ocr_result_{timestamp}.csv"
    )

    csv_df.to_csv(
        csv_path,
        index=False,
        encoding="utf-8-sig"
    )

    # Final Output
    final_output = f"""
DocOCR Vision

Detected Language:
{language_name}

Extracted Text:
{predicted_text}

Saved Files:
JSON → {json_path}

CSV → {csv_path}
"""

    return (
        final_output,
        topk_output,
        json_path,
        csv_path
    )


